public class GiftInfoBean {

    private Long goodsId;

    private int goodsPicId;

    private String goodsName;

    private String goodsPrices;

    public Long getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Long goodsId) {
        this.goodsId = goodsId;
    }

    public int getGoodsPicId() {
        return goodsPicId;
    }

    public void setGoodsPicId(int goodsPicId) {
        this.goodsPicId = goodsPicId;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsPrices() {
        return goodsPrices;
    }

    public void setGoodsPrices(String goodsPrices) {
        this.goodsPrices = goodsPrices;
    }
}
