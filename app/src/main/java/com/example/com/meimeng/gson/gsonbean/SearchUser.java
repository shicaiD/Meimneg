public class SearchUser {

    private Long uid;

    private String nickname;

    private Long headPic;

    private Integer score;

    private Integer age;

    private Integer height;

    private String city;

    private Integer ideStatus;

    private Integer eduStatus;

    private Integer jobStatus;

    private Integer marStatus;

    private Integer proStatus;


    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public Long getHeadPic() {
        return headPic;
    }

    public void setHeadPic(Long headPic) {
        this.headPic = headPic;
    }

    public Integer getScore() {
        return score;
    }

    public void setScore(Integer score) {
        this.score = score;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public Integer getIdeStatus() {
        return ideStatus;
    }

    public void setIdeStatus(Integer ideStatus) {
        this.ideStatus = ideStatus;
    }

    public Integer getEduStatus() {
        return eduStatus;
    }

    public void setEduStatus(Integer eduStatus) {
        this.eduStatus = eduStatus;
    }

    public Integer getJobStatus() {
        return jobStatus;
    }

    public void setJobStatus(Integer jobStatus) {
        this.jobStatus = jobStatus;
    }

    public Integer getMarStatus() {
        return marStatus;
    }

    public void setMarStatus(Integer marStatus) {
        this.marStatus = marStatus;
    }

    public Integer getProStatus() {
        return proStatus;
    }

    public void setProStatus(Integer proStatus) {
        this.proStatus = proStatus;
    }
}
